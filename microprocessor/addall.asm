TITLE Add all the elements of table
.MODEL SMALL
.STACK 32
.DATA
SUM DW ?
NUM DW 100,200,300,400,500,2000,9000,9000,9000,9000
LEN DB 10

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

LEA BX, NUM
MOV CH, 0
MOV CL, LEN
MOV AX, 0
STEP1: ADD AX, [BX] 
ADD BX, 02
LOOP STEP1
MOV SUM, AX

MOV BX, 10
MOV CX, 0
STEP2: MOV DX, 0
INC CX
DIV BX
PUSH DX
CMP AX, 0
JNE STEP2

;;DISPLAY
MOV AH, 02H
DISP: POP DX
OR DX, 30H ;USE OR, ADD BOTH OKAY
INT 21H
LOOP DISP

MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN