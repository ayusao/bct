TITLE Display string using character reading
	.MODEL SMALL
	.STACK 32
	.DATA
STRING DB "Programming is Fun",'$'
LEN DW $-STRING-1
	.CODE
MAIN PROC 
	MOV AX, @DATA
	MOV DS, AX
	
	MOV CX, LEN
	MOV SI, OFFSET STRING
BACK:	MOV DL, [SI]
	MOV AH, 02H; SENDS THE CHARACTER OF DL TO OUTPUT DEVICE
	INT 21H
	INC SI
	LOOP BACK
	
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
	END MAIN