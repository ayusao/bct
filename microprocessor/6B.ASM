TITLE Add 10 16-bit numbers
	.MODEL SMALL
	.STACK 32
	.DATA
NUM DW 1234H, 2345H,3456H,4567H,5678H,6789H,7890H,8901H,9012H,0123H
SUML DW 0
SUMH DW 0

	.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	
	MOV CX, 10D
	MOV AX, 0000H
	MOV SI, OFFSET NUM
BACK:	ADD AX, [SI]
	MOV SUML, AX
	JNC NOCARRY
	INC SUMH
NOCARRY:	ADD SI, 02D
	LOOP BACK
	
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
	END MAIN