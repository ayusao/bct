TITLE Add ten 16-bit data of two tables and store in the third
	.MODEL SMALL
	.STACK 64
	.DATA
NUM1 DW 1234H, 2345H,3456H,4567H,5678H,6789H,7890H,8901H,9012H,0123H
NUM2 DW 1111H, 2222H,3333H,4444H,5555H,6666H,7777H,8888H,9999H,0000H
SUM DW 10 DUP(?) ; OR DUP(0)

	.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	
	MOV CX, 10D
	MOV SI, OFFSET NUM1
	MOV DI, OFFSET NUM2
BACK:	MOV AX, 0000H
	ADD AX, [SI]
	ADD AX, [DI]
	PUSH AX
	ADD SI, 2D
	ADD DI, 2D
	LOOP BACK
	
	MOV SI, OFFSET [SUM+20D]
	MOV CX, 10D
STORE:	POP[SI]
	MOV AX, [SI]
	SUB SI, 2D
	LOOP STORE
	
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
	END MAIN