TITLE Store 1FFF if smaller else 0000H
	.MODEL SMALL
	.STACK 32
	.DATA
NUM1 DW 1234H, 2345H,3456H,4567H,5678H,6789H,7890H,8901H,9012H,0123H
NUM2 DW 1001H, 2822H,4444H,5555H,7777H,6699H,2194H,9A32H,334EH,34F3H 
SUM DW 10 DUP(?) ; OR DUP(0)

	.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	
	MOV CX, 10D
	MOV SI, OFFSET NUM1
	MOV DI, OFFSET NUM2
	
BACK:	MOV DX, [SI]
	CMP DX, [DI]
	JNC NOTSMALLER
	MOV AX, 1FFFH
	JMP JUMP
NOTSMALLER:	MOV AX, 0000H
JUMP:	PUSH AX
	ADD SI, 2D
	ADD DI, 2D
	LOOP BACK
	
	MOV SI, OFFSET [SUM+18D]
	MOV CX, 10D
STORE:	POP[SI]
	MOV AX, [SI]
	SUB SI, 2D
	LOOP STORE
	
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
	END MAIN