TITLE Multiplication table of a number 
	.MODEL SMALL
	.STACK 32
	.DATA
NUM DW 09H
NUM1 DW 10 DUP(0) ; OR DUP(0)

	.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	MOV SI, OFFSET NUM1
    MOV CX, 10H
	MOV BH, 01H

AGAIN:	MOV AX, NUM
	MUL  BH
	PUSH AX
	POP [SI]
	ADD SI, 2D
	MOV AX, 0000H
	INC BH
	LOOP  AGAIN
 
	MOV AH, 04CH
	INT 21H
MAIN ENDP
	END MAIN