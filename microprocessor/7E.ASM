TITLE To convert to uppercase
	.MODEL SMALL
	.STACK 32
	.DATA
STRING LABEL BYTE
MAXLEN DB 20
ACTLEN DB ?
KBINPUT DB 20 DUP('$')
	.CODE
MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,0AH
	LEA DX, STRING
	INT 21H
	
	MOV CX,0
	MOV CL, STRING
	MOV AH,0
	MOV BX,2
HERE:	MOV AL,[STRING+BX]
	CMP AL,'a'
	JB NEXT
	CMP AL,'z'
	JA NEXT
	SUB AL,20H
	
NEXT:	MOV [STRING+BX],AL
	INC BX
	LOOP HERE

	MOV AH,02H
	MOV DL,0DH
	INT 21H
	MOV DL,0AH
	INT 21H
	
	MOV AH,09H
	LEA DX, STRING+2
	INT 21H
	
	MOV AH, 4CH
	INT 21H
	
MAIN ENDP
	END MAIN