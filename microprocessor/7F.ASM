
TITLE READ STR AND DISPLAY EACH WORD IN A SEPARATE LINE
	.STACK 32
	.MODEL SMALL
	.DATA
STRING DB 50 DUP (?)
	.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	
	MOV SI, OFFSET STRING
	MOV BX, 0
	MOV AH, 01H
TOP:	INT 21H
	CMP AL, 0DH
	JZ DOWN
	MOV [SI], AL ; STORED IN AL THATS WHY
	INC SI
	INC BX
	JMP TOP
	
DOWN:	MOV DL, 0DH
	MOV AH, 02H
	INT 21H
	
	;CHECK FOR SPACE
	MOV AH, 02H
	MOV CX, BX
	MOV SI, OFFSET STRING
BACK:	MOV DL, [SI]
	CMP DL, 20H ; SPACE CHARACTER
	JZ NEXTWORD
	INT 21H
	INC SI
	LOOP BACK
	JMP SKIP
	
NEXTWORD:	INC SI
	MOV AH, 02H
	MOV DL, 0DH
	INT 21H
	MOV DL, 0AH
	INT 21H
	DEC CX
	JMP BACK
	
SKIP:	MOV AH, 4CH
	INT 21H
MAIN ENDP
	END MAIN